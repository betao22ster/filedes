package br.com.marcelo.filedes.file;

public class Col {
	private String name;
	private int col;

	public Col(String name, int col) {
		super();
		this.name = name;
		this.col = col;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCol() {
		return col;
	}

	public void setCol(int col) {
		this.col = col;
	}

}
